# Basics Module

# NumPy Basics

import numpy as np

# Create arrays
a = np.array([1, 2, 3])
b = np.array([4, 5, 6])

print("a:", a)
print("b:", b)

# Addition
print("Add:", a + b)

# Multiplication
print("Multiply:", a * b)

# Dot Product
print("Dot:", np.dot(a, b))

# Scalar operations
print("Scalar Multiply:", a * 2)